function wireSoundToggles() {
  document.querySelectorAll(".sound-toggle").forEach((button) => {
    button.addEventListener("click", () => {
      const frame = button.closest(".video-frame");
      const enableSound = button.getAttribute("aria-pressed") !== "true";
      button.setAttribute("aria-pressed", String(enableSound));
      button.textContent = enableSound ? "Mute" : "Unmute";

      const video = frame?.querySelector("video");
      if (video) {
        video.muted = !enableSound;
        video.volume = enableSound ? 1 : 0;
        if (enableSound) video.play().catch(() => {});
        return;
      }

      const iframe = frame?.querySelector("iframe");
      if (!iframe?.contentWindow) return;
      const source = iframe.getAttribute("src") || "";
      if (source.includes("youtube")) {
        const command = enableSound ? "unMute" : "mute";
        iframe.contentWindow.postMessage(JSON.stringify({ event: "command", func: command, args: [] }), "*");
        return;
      }
      iframe.contentWindow.postMessage(JSON.stringify({ method: "setMuted", value: !enableSound }), "*");
      iframe.contentWindow.postMessage(JSON.stringify({ method: "setVolume", value: enableSound ? 1 : 0 }), "*");
    });
  });
}

function wireVideoControls() {
  const estimatedDurations = new WeakMap();

  const updateTimeline = (frame, progress) => {
    frame.style.setProperty("--video-progress", `${Math.max(0, Math.min(100, progress))}%`);
  };

  const iframeCommand = (iframe, command) => {
    iframe.contentWindow.postMessage(JSON.stringify({ method: command }), "*");
    iframe.contentWindow.postMessage(JSON.stringify({ event: "command", func: command === "play" ? "playVideo" : "pauseVideo", args: [] }), "*");
  };

  const getEmbedTime = (frame) => {
    const duration = estimatedDurations.get(frame) || 60;
    if (frame.dataset.pausedAt) return Number(frame.dataset.pausedAt);
    const start = Number(frame.dataset.timelineStart || performance.now());
    return ((performance.now() - start) / 1000) % duration;
  };

  const playFrame = (frame) => {
    const video = frame.querySelector("video");
    if (video) {
      video.play().catch(() => {});
      frame.classList.add("is-playing");
      return;
    }

    const iframe = frame.querySelector("iframe");
    if (!iframe?.contentWindow) return;
    const pausedAt = Number(frame.dataset.pausedAt || 0);
    frame.classList.add("is-playing");
    frame.dataset.timelineStart = String(performance.now() - pausedAt * 1000);
    delete frame.dataset.pausedAt;
    iframeCommand(iframe, "play");
  };

  const pauseFrame = (frame) => {
    const video = frame.querySelector("video");
    if (video) {
      video.pause();
      frame.classList.remove("is-playing");
      return;
    }

    const iframe = frame.querySelector("iframe");
    if (!iframe?.contentWindow) return;
    const pausedAt = getEmbedTime(frame);
    frame.dataset.pausedAt = String(pausedAt);
    frame.classList.remove("is-playing");
    iframeCommand(iframe, "pause");
  };

  const toggleFrame = (frame) => {
    if (frame.classList.contains("is-playing")) {
      pauseFrame(frame);
    } else {
      playFrame(frame);
    }
  };

  const seekFrame = (frame, event) => {
    const bar = event.target.closest(".video-timeline");
    if (!bar) return;
    const rect = bar.getBoundingClientRect();
    const ratio = rect.width ? (event.clientX - rect.left) / rect.width : 0;
    const video = frame.querySelector("video");
    if (video?.duration) {
      video.currentTime = Math.max(0, Math.min(video.duration, ratio * video.duration));
      updateTimeline(frame, ratio * 100);
      return;
    }

    const iframe = frame.querySelector("iframe");
    if (!iframe?.contentWindow) return;
    const duration = estimatedDurations.get(frame) || 60;
    const time = Math.max(0, Math.min(duration, ratio * duration));
    frame.dataset.timelineStart = String(performance.now() - time * 1000);
    delete frame.dataset.pausedAt;
    frame.classList.add("is-playing");
    iframe.contentWindow.postMessage(JSON.stringify({ method: "setCurrentTime", value: time }), "*");
    iframe.contentWindow.postMessage(JSON.stringify({ event: "command", func: "seekTo", args: [time, true] }), "*");
    iframeCommand(iframe, "play");
    updateTimeline(frame, ratio * 100);
  };

  document.querySelectorAll(".video-frame").forEach((frame) => {
    const video = frame.querySelector("video");
    const timeline = frame.querySelector(".video-timeline");
    timeline?.addEventListener("click", (event) => seekFrame(frame, event));

    if (video) {
      const setProgress = () => {
        if (!video.duration) return;
        updateTimeline(frame, (video.currentTime / video.duration) * 100);
      };
      video.addEventListener("timeupdate", setProgress);
      video.addEventListener("loadedmetadata", setProgress);
      video.addEventListener("play", () => frame.classList.add("is-playing"));
      video.addEventListener("pause", () => frame.classList.remove("is-playing"));
      return;
    }

    const iframe = frame.querySelector("iframe");
    if (!iframe) return;
    estimatedDurations.set(frame, frame.classList.contains("video-frame--portrait") ? 22 : 60);
    if (frame.classList.contains("is-playing") && !frame.dataset.timelineStart) {
      frame.dataset.timelineStart = String(performance.now());
    }
  });

  document.querySelectorAll(".video-click-target").forEach((button) => {
    button.addEventListener("click", () => {
      const frame = button.closest(".video-frame");
      if (frame) toggleFrame(frame);
    });
  });

  document.querySelectorAll(".video-play").forEach((button) => {
    button.addEventListener("click", () => {
      const frame = button.closest(".video-frame");
      if (frame) playFrame(frame);
    });
  });

  const tickIframeTimelines = () => {
    document.querySelectorAll(".video-frame.is-playing iframe").forEach((iframe) => {
      const frame = iframe.closest(".video-frame");
      if (!frame) return;
      const duration = estimatedDurations.get(frame) || 60;
      const start = Number(frame.dataset.timelineStart || performance.now());
      const elapsed = ((performance.now() - start) / 1000) % duration;
      updateTimeline(frame, (elapsed / duration) * 100);
    });
    window.requestAnimationFrame(tickIframeTimelines);
  };
  tickIframeTimelines();
}

function wireInteractions() {
  const defaultProject = projects[0];
  const defaultRed = getComputedStyle(document.documentElement).getPropertyValue("--red").trim() || defaultProject.color;

  document.querySelectorAll("[data-project-link]").forEach((link) => {
    link.addEventListener("mouseenter", () => {
      const project = projects.find((item) => item.id === link.dataset.projectLink);
      if (project) document.documentElement.style.setProperty("--red", project.color);
    });
    link.addEventListener("mouseleave", () => {
      document.documentElement.style.setProperty("--red", defaultRed);
    });
  });

  const setActiveSection = (id) => {
    const project = projects.find((item) => item.id === id);
    document.querySelectorAll("[data-project-link]").forEach((link) => {
      link.classList.toggle("is-current", link.dataset.projectLink === id);
    });
    document.querySelectorAll("[data-section-link]").forEach((link) => {
      link.classList.toggle("is-current", link.dataset.sectionLink === id);
    });
    if (project) {
      document.documentElement.style.setProperty("--red", project.color);
    } else if (id === "awards") {
      document.documentElement.style.setProperty("--red", defaultRed);
    } else {
      document.documentElement.style.setProperty("--red", defaultRed);
    }
  };

  const trackedSections = Array.from(document.querySelectorAll(".case, #awards"));
  let activeTicking = false;
  const updateActiveFromScroll = () => {
    const marker = window.innerHeight * 0.68;
    let activeId = null;
    let closestTop = Number.NEGATIVE_INFINITY;

    trackedSections.forEach((section) => {
      const rect = section.getBoundingClientRect();
      if (rect.top <= marker && rect.bottom > 80 && rect.top > closestTop) {
        closestTop = rect.top;
        activeId = section.id;
      }
    });

    if (!activeId) {
      const firstVisible = trackedSections.find((section) => section.getBoundingClientRect().bottom > 80);
      activeId = firstVisible ? firstVisible.id : null;
    }

    setActiveSection(activeId);
    activeTicking = false;
  };

  const queueActiveUpdate = () => {
    if (activeTicking) return;
    activeTicking = true;
    window.requestAnimationFrame(updateActiveFromScroll);
  };

  window.addEventListener("scroll", queueActiveUpdate, { passive: true });
  window.addEventListener("resize", queueActiveUpdate);
  updateActiveFromScroll();

  const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-revealed");
        revealObserver.unobserve(entry.target);
      }
    });
  }, { rootMargin: "0px 0px -10% 0px", threshold: 0.05 });

  document.querySelectorAll(".reveal").forEach((el) => revealObserver.observe(el));

  const railToggle = document.querySelector(".rail-toggle");
  railToggle?.addEventListener("click", () => {
    const collapsed = document.body.classList.toggle("rail-collapsed");
    railToggle.setAttribute("aria-expanded", String(!collapsed));
  });
}

let dogAudioContext;
const dogBarkCooldowns = new Map();

function getDogAudioContext() {
  const AudioContextClass = window.AudioContext || window.webkitAudioContext;
  if (!AudioContextClass) return null;
  if (!dogAudioContext) dogAudioContext = new AudioContextClass();
  if (dogAudioContext.state === "suspended") dogAudioContext.resume().catch(() => {});
  return dogAudioContext;
}

function makeNoiseBuffer(context, duration) {
  const frameCount = Math.max(1, Math.floor(context.sampleRate * duration));
  const buffer = context.createBuffer(1, frameCount, context.sampleRate);
  const data = buffer.getChannelData(0);

  for (let i = 0; i < frameCount; i += 1) {
    data[i] = Math.random() * 2 - 1;
  }

  return buffer;
}

function barkPulse(context, start, profile, phrase = {}) {
  const oscillatorA = context.createOscillator();
  const oscillatorB = context.createOscillator();
  const oscillatorGain = context.createGain();
  const filter = context.createBiquadFilter();
  const barkFormant = context.createBiquadFilter();
  const noise = context.createBufferSource();
  const noiseGain = context.createGain();
  const output = context.createGain();
  const end = start + profile.duration;
  const frequency = profile.frequency * (phrase.frequencyScale || 1);

  oscillatorA.type = profile.wave;
  oscillatorB.type = "square";
  oscillatorA.frequency.setValueAtTime(frequency * profile.scoop, start);
  oscillatorA.frequency.exponentialRampToValueAtTime(frequency, start + 0.012);
  oscillatorA.frequency.setValueAtTime(frequency * profile.step, start + profile.duration * 0.42);
  oscillatorA.frequency.exponentialRampToValueAtTime(frequency * profile.drop, end);
  oscillatorB.frequency.setValueAtTime(frequency * 2.05, start);
  oscillatorB.frequency.setValueAtTime(frequency * 1.58, start + profile.duration * 0.5);
  oscillatorB.frequency.exponentialRampToValueAtTime(frequency * 1.08, end);

  filter.type = "bandpass";
  filter.frequency.setValueAtTime(profile.noiseFrequency, start);
  filter.Q.setValueAtTime(profile.noiseQ, start);

  barkFormant.type = "bandpass";
  barkFormant.frequency.setValueAtTime(profile.formant, start);
  barkFormant.Q.setValueAtTime(profile.formantQ, start);

  noise.buffer = makeNoiseBuffer(context, profile.duration);

  oscillatorGain.gain.setValueAtTime(0.0001, start);
  oscillatorGain.gain.exponentialRampToValueAtTime(profile.toneLevel, start + 0.006);
  oscillatorGain.gain.exponentialRampToValueAtTime(profile.toneLevel * 0.55, start + 0.026);
  oscillatorGain.gain.exponentialRampToValueAtTime(0.0001, end);

  noiseGain.gain.setValueAtTime(0.0001, start);
  noiseGain.gain.exponentialRampToValueAtTime(profile.noiseLevel, start + 0.004);
  noiseGain.gain.exponentialRampToValueAtTime(profile.noiseLevel * 0.48, start + 0.03);
  noiseGain.gain.exponentialRampToValueAtTime(0.0001, end);

  output.gain.setValueAtTime(profile.volume * (phrase.volumeScale || 1), start);
  output.gain.exponentialRampToValueAtTime(0.0001, end + 0.025);

  oscillatorA.connect(oscillatorGain);
  oscillatorB.connect(oscillatorGain);
  oscillatorGain.connect(output);
  noise.connect(filter);
  filter.connect(barkFormant);
  barkFormant.connect(noiseGain);
  noiseGain.connect(output);
  output.connect(context.destination);

  oscillatorA.start(start);
  oscillatorA.stop(end + 0.03);
  oscillatorB.start(start);
  oscillatorB.stop(end + 0.03);
  noise.start(start);
  noise.stop(end + 0.03);
}

function playDogBark(kind) {
  const now = performance.now();
  const previous = dogBarkCooldowns.get(kind) || 0;
  if (now - previous < 850) return;
  dogBarkCooldowns.set(kind, now);

  const context = getDogAudioContext();
  if (!context) return;

  const profiles = {
    lola: {
      wave: "triangle",
      frequency: 1180,
      scoop: 0.72,
      step: 1.34,
      drop: 0.86,
      duration: 0.105,
      toneLevel: 0.22,
      noiseLevel: 0.08,
      noiseFrequency: 5100,
      noiseQ: 12,
      formant: 1850,
      formantQ: 9,
      volume: 0.22,
      phrases: [
        { gap: 0, frequencyScale: 1, volumeScale: 1 },
        { gap: 0.115, frequencyScale: 1.26, volumeScale: 0.92 },
        { gap: 0.255, frequencyScale: 0.9, volumeScale: 0.78 }
      ]
    },
    neal: {
      wave: "square",
      frequency: 850,
      scoop: 0.82,
      step: 1.5,
      drop: 0.72,
      duration: 0.13,
      toneLevel: 0.19,
      noiseLevel: 0.11,
      noiseFrequency: 4100,
      noiseQ: 9,
      formant: 1350,
      formantQ: 8,
      volume: 0.21,
      phrases: [
        { gap: 0, frequencyScale: 1, volumeScale: 1 },
        { gap: 0.145, frequencyScale: 0.78, volumeScale: 0.92 },
        { gap: 0.31, frequencyScale: 1.18, volumeScale: 0.76 }
      ]
    }
  };
  const profile = profiles[kind] || profiles.lola;
  const start = context.currentTime + 0.01;

  profile.phrases.forEach((phrase) => {
    barkPulse(context, start + phrase.gap, profile, phrase);
  });
}

function wireDogBarks() {
  const dogMap = [
    [".hero-dog-wrap--lola", "lola"],
    [".hero-dog-wrap--neal", "neal"]
  ]
    .map(([selector, kind]) => {
      const dog = document.querySelector(selector);
      return dog ? { dog, kind } : null;
    })
    .filter(Boolean);

  if (!dogMap.length) return;

  const setDogHover = (dog, kind, hovered) => {
    const wasHovered = dog.classList.contains("is-dog-hovered");
    dog.classList.toggle("is-dog-hovered", hovered);
    if (hovered && !wasHovered) playDogBark(kind);
  };

  document.addEventListener("pointermove", (event) => {
    dogMap.forEach(({ dog, kind }) => {
      const rect = dog.getBoundingClientRect();
      const pad = 18;
      const hovered =
        event.clientX >= rect.left - pad &&
        event.clientX <= rect.right + pad &&
        event.clientY >= rect.top - pad &&
        event.clientY <= rect.bottom + pad;

      setDogHover(dog, kind, hovered);
    });
  }, { passive: true });

  document.addEventListener("pointerleave", () => {
    dogMap.forEach(({ dog, kind }) => setDogHover(dog, kind, false));
  });

  dogMap.forEach(({ dog, kind }) => {
    dog.addEventListener("focusin", () => setDogHover(dog, kind, true));
    dog.addEventListener("focusout", () => setDogHover(dog, kind, false));
  });
}


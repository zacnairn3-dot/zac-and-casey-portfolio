function withAutoplay(src, autoplay) {
  const separator = src.includes("?") ? "&" : "?";
  if (!autoplay) {
    if (src.includes("youtube")) {
      return `${src}${separator}autoplay=0&mute=0&playsinline=1&rel=0&modestbranding=1&controls=1&enablejsapi=1`;
    }
    return `${src}${separator}autoplay=0&muted=0&autopause=0&playsinline=1&title=0&byline=0&portrait=0&controls=1&dnt=1`;
  }
  if (src.includes("youtube")) {
    const id = src.split("/").pop();
    return `${src}${separator}autoplay=1&mute=1&loop=1&playlist=${id}&playsinline=1&rel=0&modestbranding=1&controls=1&enablejsapi=1`;
  }
  return `${src}${separator}autoplay=1&muted=1&loop=1&autopause=0&playsinline=1&title=0&byline=0&portrait=0&controls=0&background=1&dnt=1`;
}

function soundToggleLabel(item) {
  return `Toggle sound for ${item.title}`;
}

function shouldRenderSoundToggle(item) {
  return !(item.type === "embed" && item.src.includes("youtube"));
}

function renderSoundToggle(item) {
  if (!shouldRenderSoundToggle(item)) return "";
  const soundEnabled = !(item.autoplay || item.type === "video");
  return `<button class="sound-toggle" type="button" aria-label="${soundToggleLabel(item)}" aria-pressed="${soundEnabled ? "true" : "false"}">${soundEnabled ? "Mute" : "Unmute"}</button>`;
}

function renderTimeline(item) {
  const label = item.manual ? "Waiting to play" : "Timeline";
  return `<button class="video-timeline" type="button" aria-label="${label}"><i></i></button>`;
}

function renderPlayButton(item) {
  if (!item.manual) return "";
  return `<button class="video-play" type="button" aria-label="Play ${item.title}"><span>Play</span></button>`;
}

function renderClickTarget(item) {
  return `<button class="video-click-target" type="button" aria-label="Play or pause ${item.title}"></button>`;
}

function renderLyrics(item, className = "lyric-reveal") {
  return item.lyrics?.length
    ? `<ol class="${className}" aria-label="${item.title} lyrics">
        ${item.lyrics.map((line, index) => `<li style="--line: ${index}">${line}</li>`).join("")}
      </ol>`
    : "";
}

